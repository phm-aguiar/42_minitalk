#include "libft.h"

char	*ft_itobin(unsigned int c)
{
	char	*bin;
	int		i;

	i = 7;
	bin = ft_strnew(8);
	while (i >= 0)
	{
		if (c & (1 << i))
			bin[7 - i] = '1';
		else
			bin[7 - i] = '0';
		i--;
	}
	return (bin);
}
